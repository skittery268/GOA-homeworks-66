// შემდეგ გადაურეთ arr მასივს for ციკლით, ყველა მნიშვენლობაზე გამოიძახეთ რიგრიგობით helper ფუნქცია და შეამოწმეთ 
// helper ფუნქციის მიერ დაბრუნებული მნიშვნელობა უდრის თუ არა true, თუ უდრის მაშინ დაამატეთ გადაცემული 
