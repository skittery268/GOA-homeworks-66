// გამოძახებისთვის შექმენით ნებიუსმიერი ტიპის მასივი, და ფუნქცია რომელსაც თითოეული მნიშვნელობის შესამოწმებლად გამოიყენებთ მაგალითად

// მასივი [1,2,3,4,5]

// ფუნქცია: ამოწმებს არგუმენტად მიღებულ რიცხვის არის თუ არა ლუწი

// return num % 2 === 0

const filter = function(arr, helper) {
    const result = [];

    for (let i = 0; i < arr.length; i++) {
        if (helper(arr[i]) === true) {
            result.push(arr[i]);
        };
    };

    return result;
};

console.log(filter([1, 2, 3, 4, 5], function(value) {
    return value % 2 == 0;
}));

