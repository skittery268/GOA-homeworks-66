function App() {
  const newArr = ["ლევანი", "ლაშა", "სერგო", "გიორგი", "ნიკა"];
  return (
    <main>
      <ul>
        {
          newArr.map((value => <li>{value}</li>))
        }
      </ul>
    </main>
  )
}

export default App
