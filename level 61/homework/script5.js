// მაგალითად: მასივი --- ['Nino', 'Alice', 'Charlie'] Hello Nino, Hello Alice, Hello Charlie

const names = ['Nino', 'Alice', 'Charlie'];

for (let i = 0; i < names.length; i++) {
    console.log(`Hello ${names[i]}`);
};

