// 1) რა არის HTTP?
// რა არის HTTP Request/Response და როგორ გამოიყურება ისინი.

// HTTP (HyperText Transfer Protocol) — არის პროტოკოლი, რომელიც განსაზღვრავს, თუ როგორ ხდება მონაცემების გადაცემა ვებზე (ინტერნეტში) კლიენტსა და სერვერს შორის.
// HTTP Request — ეს არის კლიენტისგან (მაგ. ბრაუზერისგან) სერვერისადმი გაგზავნილი მოთხოვნა.
// HTTP Response — ეს არის სერვერისგან კლიენტისთვის დაბრუნებული პასუხი.

