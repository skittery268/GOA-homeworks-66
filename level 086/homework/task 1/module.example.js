export const sum = (a, b) => {
    return a + b;
};

export const multiple = (a, b) => {
    return a * b;
};

export const difference = (a, b) => {
    return a - b;
};

export const message = (name, age) => {
    return `Hello ${name}, your age ${age}`;
};

export const greet = (name) => {
    return `Hello ${name}`;
};

